.8086
.MODEL SMALL
.STACK
.DATA
	MSG1 DB "ENTER THE STRING:- $"
	MSG2 DB "STRING WITH CHANGED CASE:- $"
	INPUT1 DB 100 DUP(0)
	OUTPUT DB 100 DUP(0)
	LINE DB 10, 13, "$"

.CODE
	MOV AX, @DATA
	MOV DS, AX
	LEA SI, INPUT1
	LEA DI, OUTPUT
	PUSH SI
	CALL STR_IN
	POP SI
	MOV BL, 41H
	MOV BH, 5AH
	MOV CL, 61H
	MOV CH, 7AH
	MOV AH, '$'
NEXT:	MOV AL, [SI]
	CMP AL, AH
	JZ OVER
	CMP AL, BL
	JC N_UP
	CMP BH, AL
	JC N_UP
	ADD AL, 20H
	MOV [DI], AL
	INC SI
	INC DI
	JMP NEXT
N_UP:   CMP AL, CL
	JC N_LOW
	CMP CH, AL
	JC N_LOW
	SUB AL, 20H
	MOV [DI], AL
	INC SI
	INC DI
	JMP NEXT
N_LOW:  MOV [DI], AL
	INC SI
	INC DI
	JMP NEXT
OVER:   MOV BYTE PTR [DI], '$'
	LEA DX, MSG2
	MOV AH, 09H
	INT 21H
	LEA DX, OUTPUT
        MOV AH, 09H
        INT 21H
	MOV AH, 4CH
	INT 21H

STR_IN:
	PUSH BP
	MOV BP, SP
	MOV SI, [BP + 04]
	MOV CL, 13
	LEA DX, MSG1
	MOV AH, 09H
	INT 21H
CONTI:	MOV AH, 01H
	INT 21H
	CMP AL, CL
	JZ IN_COM
	MOV [SI], AL
	INC SI
	JMP CONTI
IN_COM: MOV BYTE PTR [SI], '$'
	POP BP
	RET

END
