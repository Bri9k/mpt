.8086
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB  "ENTER THE STRING : $"
MSG2 DB  "COPIED STRING IS : $"
STR1 DB 20 DUP(?)
STR2 DB 20 DUP(?)

.CODE
MOV AX, @DATA
MOV DS, AX
MOV ES, AX

LEA DX, MSG1
MOV AH, 09H
INT 21H

LEA DI, STR1
CALL INPUT
CALL COPY

LEA DX, MSG2
MOV AH, 09H
INT 21H

LEA DX, STR2
MOV AH, 09H
INT 21H

MOV AX, 4C00H
INT 21H

INPUT: 
        CLD
I1:     MOV AH, 01H
        INT 21H
        CMP AL, 0DH
        JZ I2
        STOSB
        JMP I1
I2:     MOV AL, "$"
        MOV [DI], AL
        RET

COPY:
	CLD
	LEA SI, STR1
	LEA DI, STR2
        MOV BL, "$"
C1:	CMP [SI], BL
        JZ C2
	MOVSB
	JMP C1
C2:	MOV [DI], BL
	RET

END

